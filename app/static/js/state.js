/**
 * @file state.js
 * @description Manages the global reactive state of the NarrateImage application.
 * This module acts as the single source of truth for the frontend, tracking the
 * active script, the processed data structure, UI toggle states, and user selections.
 */

/**
 * Global application state object.
 * @namespace state
 */
export const state = {
    /** 
     * The filename of the currently selected and loaded script (e.g., "my_video.md").
     * Set to null when in the initial script selection view.
     * @type {string|null} 
     */
    selectedScript: null,

    /** 
     * An array of segment objects representing the script's visual mapping.
     * Each object typically contains:
     * - id: {number} The segment ID (AI-generated or paragraph index).
     * - text: {string} The narration text for this segment.
     * - keywords: {string[]} Array of search keywords (with optional 'provider:' prefix).
     * - images: {Array<Object|string>} List of associated image metadata or paths.
     * - downloaded_keywords: {string[]} Keywords that already have local images.
     * @type {Array<Object>} 
     */
    processedSegments: [],

    /** 
     * Flag indicating if the application is in 'Edit Mode' (showing the raw textarea).
     * When false, the application is in 'Segments Mode' (showing interactive blocks).
     * @type {boolean} 
     */
    isEditMode: false,

    /** 
     * Flag indicating if the current `processedSegments` were generated by the AI pipeline.
     * If false, segments are generated by simple paragraph splitting.
     * Reset to false whenever the script text is modified in Edit Mode.
     * @type {boolean} 
     */
    isAiProcessed: false,

    /** 
     * The 0-based index of the currently active/selected segment in the main view.
     * Used to determine which images to show in the right sidebar.
     * @type {number} 
     */
    activeSegmentIndex: -1,

    /** 
     * A set of unique file paths for images currently selected by the user.
     * Selected images can be pinned or deleted in bulk.
     * @type {Set<string>} 
     */
    selectedImagePaths: new Set(),

    /** 
     * A set of supported API-based image providers. 
     * Used by the download queue to distinguish between scraper-based and API-based requests.
     * @type {Set<string>} 
     */
    API_PROVIDERS: new Set(["picsum", "dicebear", "robohash", "uiavatars", "nasa", "met"])
};
